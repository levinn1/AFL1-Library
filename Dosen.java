package AFL;

public class Dosen {
    private String nama;
    private String NIK;

    public Dosen(String nama, String NIK) {
        this.nama = nama;
        this.NIK = NIK;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getNIK() {
        return NIK;
    }

    public void setNIK(String NIK) {
        this.NIK = NIK;
    }
}


